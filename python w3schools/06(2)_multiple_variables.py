# Many Values to Multiple Variables
name,age,dob = "Nithi" , 22,"29-10-2001"
print(name,age,dob)

# multiple variables to single value
name1 , name2 , name3 = "nithi" , "jennie" , "amma"
print(name1,name2,name3)

# Unpack a Collection
fruit= ["apple" , "banana" , "Orange"]
x,y,z = fruit
print(x,y,z)